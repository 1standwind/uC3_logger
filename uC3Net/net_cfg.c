/*******************************
      Initialize Network
 *******************************/
ER net_setup(void)
{
    ER ercd;

    /* Initialize TCP/IP Stack */
    ercd = net_ini();
    if (ercd != E_OK) {
        return ercd;
    }

    /* Initialize Network Driver */
    ercd = net_dev_ini(ID_NETIF_DEV1);
    if (ercd != E_OK) {
        return ercd;
    }


    gHTTP_FILE = (T_HTTP_FILE*)content_list;

    /* Start HTTP Task */
    sta_tsk(ID_HTTPD_TSK1, 0);

    return ercd;
}

